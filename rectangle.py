def area(a, b): 
    ''' Функция возвращает площадь прямоугольника. Пример вызова функции: 
    a = 10, b = 8 
    10 * 8 = 80
    return 80 ''' 
    return a * b 

def perimeter(a, b): 
    ''' Функция возвращает периметр прямоугольника. Пример вызова функции:
    a = 10, b = 8
    2 * (10 + 8) = 36
    return 36 ''' 
    return 2 (a + b)